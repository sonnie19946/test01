   - 解决方案：只要遇到较长的叙述，即使角色依然坐在原地没动，你也必须通过**切换景别**（全景推半身）、**改变机位**（正脸切侧脸/背面绕肩）、或**插入局部特写**（手部动作、眼神微表情、桌上的道具），强行制造新的视觉画面来为旁白提供“刷新感”。
